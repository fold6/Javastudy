package section05;

public class Ex0506 {
    public static void main(String[] args) {
        System.out.println("java");
        System.out.println("is");
        System.out.println("nice!");
        // 세미 콜론이 코드 한 줄씩을 구분해줌 (한줄로 쭉 써도 상관없음)

        /* - for문의 구성 변수지정 ; 조건식; 증감연산등..;  총 3줄의 코드가 들어감 */
        for(int i = 1; i <= 10; i++){
            //i 가 10 이하면, i를 1씩증가한다 (참이라서 실행됨)
            System.out.printf("%d ",i);
        }

    }
}
