package section03;

public class Ex0302 {
    public static void main(String[] args) {
        // 증감연산자(++,--)
        int num = 1;

        System.out.println("num\t:" + num);//1
        System.out.println("++num\t:" + ++num);//2
        System.out.println("++num\t:" + num);//2
        System.out.println("++num\t:" + num++);//2
        System.out.println("++num\t:" + num);
        
        
    }
    
}
