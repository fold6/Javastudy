public class Practice0304 {
    public static void main(String[] args) {
    //전위,후위 증감 연산자 TEST
      int num = 10;


      System.out.println("연산 결과는 11\t :" + ++num);
      System.out.println("연산 결과는 10\t :" + --num);
      System.out.println("연산 결과는 11\t :" + ++num);
      System.out.println("연산 결과는 11\t :" + num++);
      System.out.println("연산 결과는 12\t :" + num);
    }
}
