package section03;

public class Ex0301 {
    public static void main(String[] args) {
        int num = 100;
        int resultPlus = +num;
        int resultMinus = -num;

        // \t : tab 넣는 기호
        System.out.println("resultPlus\t: " + resultPlus);
        System.out.println("resultMinus\t: " + resultMinus);

        System.out.println("resultPlus\t: " + -resultPlus);
        System.out.println("resultMinus\t: " + -resultMinus);
    }
}
