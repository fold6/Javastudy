package section03;

public class Ex0303 {
    public static void main(String[] args) {
        char a = 'a';

        // 문자는 증감 연산자 사용 가능
        System.out.println("a\t:" + a);
        System.out.println("++a\t:" + ++a); // a 다음 문자인 b가 출력
        System.out.println("++a\t:" + ++a); // a 다음 문자인 b가 출력
        System.out.println("(int) a\t:" + (int) a); //c의 아스키 코드가 나타남
        
    }
    
}
