package section02;

public class Variable {
    public static void main(String[] args) {
        // 변수의 작명 방식 : camelCase (lower camel case)
        int a; // 변수(값이 변할 수 있는 수) 선언
        a = 3; // a에 3을 할당 (대입 : assignment) : 초기화
        System.out.println(a);

        int b  = 5; // 변수 정의 (위의 단계를 하나로 합침)
        System.out.println(b);
        //sout + tab 해도 sysout 단축됨

        a = 10;
        System.out.println(a);

    }
}
