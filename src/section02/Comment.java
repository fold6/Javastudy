package section02;
/*[참고]
 * 파일이름과 클래스 이름(Comment)은 같아야 함
 * public 은 클래스 중 딱 하나만 붙힐 수 있음
 * 
*/
/* [주석의 종류]
 * 1. ctrl + /는 라인 주석을 만드는 단축키
 * 2. 블럭 주석은 슬래시(/)와 아스타리스크(*) 사용
 * 3. 주의 : 
 * -주석이 없이 코드가 이해되는것이 가장 좋음
 * -코드가 수정되면 주석도 수정되어야함
 */
public class Comment {
    //자바 문구를 출력하는 메인메서드
    public static void main(String[] args) {
        System.out.println("Java!!");
    }
}
