package section18;

public class Ex1801 {
    public static void main(String[] args) {

        
        System.out.print("Java Is ");
        System.out.print("nice!");
        //print 메서드는 줄 바꿈이 없음


        //println 메서드는 주어진 내용을 출력하고 출력 후에 줄 바꿈 ,따라서 다음 출력은 새로운 줄에서 시작
        //이 경우, 문자열 끝에 \n을 붙히면 줄바꿈 됨
        System.out.print("\n");
        System.out.print("\n");
        System.out.print("===println줄바꾸기====\n");
        System.out.println("Java Is");
        System.out.println("nice!");

    }
    
}
